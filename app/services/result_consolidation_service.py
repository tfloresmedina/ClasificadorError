class ResultConsolidationService:


    @staticmethod
    def consolidar(


        analisis,

        metricas,

        errores,

        perfil,

        alertas,

        resumen
    ):


        return {

            'analisis':
            analisis,

            'metricas':
            metricas,

            'errores':
            errores,

            'perfil':
            perfil,

            'alertas':
            alertas,

            'resumen':
            resumen
        }